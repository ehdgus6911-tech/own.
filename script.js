function renderCategory(index) {
  const category = categories[index];
  categoryTitleEl.textContent = `${category.emoji} ${category.name}`;
  categoryStepEl.textContent = `${index + 1} / ${categories.length}`;

  const progressPercent = ((index + 1) / categories.length) * 100;
  progressFillEl.style.width = `${progressPercent}%`;

  questionsContainer.innerHTML = "";
  category.questions.forEach((q, qi) => {
    const card = document.createElement("div");
    card.className = "question-card";

    const text = document.createElement("div");
    text.className = "question-text";
    text.textContent = `${index * 10 + qi + 1}. ${q}`;

    const toggle = document.createElement("div");
    toggle.className = "answer-toggle";

    const label = document.createElement("label");
    const checkbox = document.createElement("input");
    checkbox.type = "checkbox";
    checkbox.checked = answers[index][qi];
    checkbox.addEventListener("change", () => {
      answers[index][qi] = checkbox.checked;
    });
    const span = document.createElement("span");
    span.textContent = "ì˜ˆ";

    label.appendChild(checkbox);
    label.appendChild(span);
    toggle.appendChild(label);

    card.appendChild(text);
    card.appendChild(toggle);
    questionsContainer.appendChild(card);
  });

  // ë²„íŠ¼ ìƒíƒœ
  prevBtn.disabled = index === 0;
  nextBtn.textContent = index === categories.length - 1 ? "ê²°ê³¼ ë³´ê¸°" : "ë‹¤ìŒ";
}

