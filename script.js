function scoreToTier(score) {
  // ì „ì²´ 60ì  ê¸°ì¤€
  if (score <= 8) return "iron";
  if (score <= 21) return "bronze";
  if (score <= 34) return "silver";
  if (score <= 40) return "gold";
  if (score <= 45) return "platinum";
  if (score <= 50) return "diamond";
  if (score <= 53) return "master";
  if (score <= 57) return "grandmaster";
  return "challenger";
}

function tierInfoById(id) {
  return tierConfig.find((t) => t.id === id);
}

function tierIndex(id) {
  return tierConfig.findIndex((t) => t.id === id);
}

// ì¹´í…Œê³ ë¦¬ 10ì  â†’ 60ì  í™˜ì‚° í›„ í‹°ì–´ ê³„ì‚°
function categoryScoreToTier(catScore) {
  const scaled = Math.round((catScore / 10) * 60);
  return scoreToTier(scaled);
}

