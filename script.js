function calculateScores() {
  const categoryScores = categories.map((cat, ci) =>
    answers[ci].reduce((sum, v) => sum + (v ? 1 : 0), 0)
  );
  const totalScore = categoryScores.reduce((a, b) => a + b, 0);
  return { categoryScores, totalScore };
}

